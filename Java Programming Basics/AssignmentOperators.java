public class AssignmentOperators {
    public static void main(String[] args) {
        /* IN JAVA WE HAVE ASSIGNMENT OPERATORS
         * 1) =
         * 2)+=
         * 3)-=
         * 4)*=
         * 5)/=
          */
          int A =10;
          System.out.println(A);
          //a = a+10
          A+=10;
          System.out.println(A);

          // a= a-10
          A-=10;
          System.out.println(A);

          // a = a*5
          A*=5;
          System.out.println(A);
          // A = A / 2;
          A/=2;

          System.out.println(A);

    }
    
}
